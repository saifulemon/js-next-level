// var is function scope
var city = "Dhaka";
console.log(city);

// let is block scope and can`t re-declare same identifier
// let variables can be reassigned but not redeclared
let shohor = "Chattogram";
console.log(shohor);

// const variables can't reassigned & redeclared
const country = "Noakhali";
country = "United States";
console.log(country);